package devoirs.devoir01;

public class devoir01 {
    public static void main(String[] args) {
        int monAge = 5;

        Boolean estMajeur;
        if (monAge < 18) {
            estMajeur = false;
        } else {
            estMajeur = true;
        }
        System.out.println("Je m'appelle Luca Meirino");
        if (estMajeur = true) {
            System.out.println("Je suis majeur.");
        } else {
            System.out.println("Je suis mineur.");
        }
    }
}
